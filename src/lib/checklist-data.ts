// ヘルパー: スキルIDからスキルとカテゴリを取得
export function findSkill(skillId: string): { skill: Skill; catKey: string } | null {
  for (const [catKey, cat] of Object.entries(CHECKLIST_DATA)) {
    const skill = cat.skills.find((s) => s.id === skillId);
    if (skill) return { skill, catKey };
  }
  return null;
}

// 全スキルIDの一覧
export function getAllSkillIds(): string[] {
  const ids: string[] = [];
  for (const cat of Object.values(CHECKLIST_DATA)) {
    for (const skill of cat.skills) {
      ids.push(skill.id);
    }
  }
  return ids;
}

// レベルラベル
export function getLevelInfo(level: SkillLevel) {
  switch (level) {
    case "b": return { label: "初級", className: "bg-green-100 text-green-700" };
    case "m": return { label: "中級", className: "bg-yellow-100 text-yellow-700" };
    case "a": return { label: "上級", className: "bg-red-100 text-red-700" };
  }
}

// カテゴリカラー
export function getCategoryColor(color: string) {
  switch (color) {
    case "base": return { dot: "bg-purple-500", bar: "bg-purple-500", border: "border-purple-400", text: "text-purple-700" };
    case "back": return { dot: "bg-red-500", bar: "bg-red-500", border: "border-red-400", text: "text-red-700" };
    case "front": return { dot: "bg-blue-500", bar: "bg-blue-500", border: "border-blue-400", text: "text-blue-700" };
    case "side": return { dot: "bg-green-500", bar: "bg-green-500", border: "border-green-400", text: "text-green-700" };
    case "special": return { dot: "bg-orange-500", bar: "bg-orange-500", border: "border-orange-400", text: "text-orange-700" };
    case "combo": return { dot: "bg-pink-500", bar: "bg-pink-500", border: "border-pink-400", text: "text-pink-700" };
    default: return { dot: "bg-gray-500", bar: "bg-gray-500", border: "border-gray-400", text: "text-gray-700" };
  }
}
