export type SkillLevel = "b" | "m" | "a"; // beginner, middle, advanced

export interface SubCheck {
  l: string; // label
}

export interface CheckItem {
  l: string;      // label
  v?: string;     // video title
  vUrl?: string;  // video URL (YouTube etc.)
  sub: SubCheck[];
}

export interface Skill {
  id: string;
  name: string;
  level: SkillLevel;
  hint: string;
  desc: string;
  checks: CheckItem[];
  difficulty_level?: number;
  default_expanded?: boolean;
}

export interface Category {
  name: string;
  color: string;
  skills: Skill[];
}

